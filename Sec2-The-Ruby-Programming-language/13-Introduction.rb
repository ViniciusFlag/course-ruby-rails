# Formas de dizer "Hello Word"
puts "Hello Word" # Hello Word, pula a linha apos o comando
print "Hello Word \n" # Hello Word, não pula linha apos o comando, usa-se o "\n"
p "Hello Word" # "Hello Word"

# Utilizando Variaveis
greeting = "Hello Word"
puts greeting

# Utilizando um metodo
def say_hello(thing_to_say)
    puts thing_to_say
end

say_hello "Hello Word"